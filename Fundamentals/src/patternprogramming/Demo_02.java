package patternprogramming;

public class Demo_02 {

	public static void main(String[] args) {
		// Showcase of pattern programming
		/** Pattern 
			A A A A A 
			A A A A A 
			A A A A A 
			A A A A A
		*/
		for (int i = 0; i < 4; i++) {
			for (int j = 0; j < 5; j++) {
				System.out.print("A" + " ");
			}
			System.out.println();
		}

	}

}
