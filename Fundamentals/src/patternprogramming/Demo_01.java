package patternprogramming;

public class Demo_01 {

	public static void main(String[] args) {
		// Showcase of pattern programming
		/** Pattern 
			* * * * * 
			* * * * * 
			* * * * * 
			* * * * * 
			* * * * * 
		*/
		for (int i = 0; i < 5; i++) {
			for (int j = 0; j < 5; j++) {
				System.out.print("*" + " ");
			}
			System.out.println();
		}
		

	}

}
