package operators;

public class RelationalOperatorDemo {
	public static void main(String[] args) {

		System.out.println(10 > 2);
		System.out.println(10 < 2);
		System.out.println(10 >= 2);
		System.out.println(10 <= 2);
		System.out.println(10 == 2); // Equality Operator
		System.out.println(10 != 2); // Non-equality Operator
	}

}
